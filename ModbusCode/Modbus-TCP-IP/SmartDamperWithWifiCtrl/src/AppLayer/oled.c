//写数据
void OLED_WrDat(unsigned char data)
{
	unsigned char i=8;
	OLED_DC=1;
  OLED_SCL=0;
  while(i--)
  {
    if(data&0x80){OLED_SDA=1;}
    else{OLED_SDA=0;}
    OLED_SCL=1; 
		__ASM("NOP");
    OLED_SCL=0;   
    data<<=1;    
  }
}

//写命令
void OLED_WrCmd(unsigned char cmd)
{
	unsigned char i=8;
	
	OLED_DC=0;
  OLED_SCL=0;
  while(i--)
  {
    if(cmd&0x80){OLED_SDA=1;}
    else{OLED_SDA=0;}
    OLED_SCL=1;
    __ASM("NOP");
    OLED_SCL=0;  
    cmd<<=1;  
  } 	
}

//设定位置信息
void OLED_Set_Pos(unsigned char x, unsigned char y)
{ 
  OLED_WrCmd(0xb0+y);
  OLED_WrCmd(((x&0xf0)>>4)|0x10);
  OLED_WrCmd((x&0x0f)|0x01); 
} 

//延时函数
void OLED_DLY_ms(unsigned int ms)
{                         
  unsigned int a;
  while(ms)
  {
    a=1335;
    while(a--);
    ms--;
  }
  return;
}

