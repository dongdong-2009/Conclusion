void OLED_Init(void) //initialization
{
	GPIO_InitTypeDef  GPIO_InitStructure;

	RCC_APB2PeriphClockCmd(RCC_APB2Periph_GPIOA|RCC_APB2Periph_GPIOC, ENABLE);	 //使能PA,PC端口时钟

	GPIO_InitStructure.GPIO_Pin = GPIO_Pin_8;				 		 //OLED_SCL-->PA.8 端口配置
	GPIO_InitStructure.GPIO_Mode = GPIO_Mode_Out_PP; 		 //推挽输出
	GPIO_InitStructure.GPIO_Speed = GPIO_Speed_50MHz;		 //IO口速度为50MHz
	GPIO_Init(GPIOA, &GPIO_InitStructure);					 	     //根据设定参数初始化GPIOA.8
	GPIO_SetBits(GPIOA,GPIO_Pin_8);						 			     //PA.8 输出高

	GPIO_InitStructure.GPIO_Pin = GPIO_Pin_7;	    		   //OLED_DC-->PC.7 端口配置, 推挽输出
	GPIO_Init(GPIOC, &GPIO_InitStructure);	  				     //推挽输出 ，IO口速度为50MHz
	GPIO_SetBits(GPIOC,GPIO_Pin_7); 						 			     //PC.7 输出高 

	GPIO_InitStructure.GPIO_Pin = GPIO_Pin_8;	    		   //OLED_RST-->PC.8 端口配置, 推挽输出
	GPIO_Init(GPIOC, &GPIO_InitStructure);	  				     //推挽输出 ，IO口速度为50MHz
	GPIO_SetBits(GPIOC,GPIO_Pin_8); 						 			     //PC.7 输出高 

	GPIO_InitStructure.GPIO_Pin = GPIO_Pin_9;	    		   //OLED_SDA-->PC.9 端口配置, 推挽输出
	GPIO_Init(GPIOC, &GPIO_InitStructure);	  				     //推挽输出 ，IO口速度为50MHz

	GPIO_SetBits(GPIOC,GPIO_Pin_9); 						 			     //PC.7 输出高 

	
	OLED_SCL=1;	
	OLED_RST=0;
	OLED_DLY_ms(50);
	OLED_RST=1;
  Set_Display_On_Off(0x00);		  // Display Off (0x00/0x01)
  Set_Display_Clock(0x80);		  // Set Clock as 100 Frames/Sec
  Set_Multiplex_Ratio(0x3F);		// 1/64 Duty (0x0F~0x3F)
  Set_Display_Offset(0x00);		  // Shift Mapping RAM Counter (0x00~0x3F)
  SetStartLine(0x00);			      // Set Mapping RAM Display Start Line (0x00~0x3F)
  Set_Charge_Pump(0x04);		    // Enable Embedded DC/DC Converter (0x00/0x04)
  SetAddressingMode(0x02);		  // Set Page Addressing Mode (0x00/0x01/0x02)
  Set_Segment_Remap(0x01);		  // Set SEG/Column Mapping     0x00???? 0x01??
  Set_Common_Remap(0x08);			  // Set COM/Row Scan Direction 0x00???? 0x08??
  Set_Common_Config(0x10);		  // Set Sequential Configuration (0x00/0x10)
  SetContrastControl(Brightness);	// Set SEG Output Current
  Set_Precharge_Period(0xF1);		// Set Pre-Charge as 15 Clocks & Discharge as 1 Clock
  Set_VCOMH(0x40);			        // Set VCOM Deselect Level
  Set_Entire_Display(0x00);		  // Disable Entire Display On (0x00/0x01)
  Set_Inverse_Display(0x00);		// Disable Inverse Display On (0x00/0x01)  
  Set_Display_On_Off(0x01);		  // Display On (0x00/0x01)
  OLED_Fill(0x00);  //
	OLED_Set_Pos(0,0); 
}

void OLED_P6x8Str(unsigned char x,unsigned char y, char ch[])
{
  unsigned char c=0,i=0,j=0;      
  while (ch[j]!='\0')
  {    
    c =ch[j]-32;
    if(x>120){x=0;y++;}
    OLED_Set_Pos(x,y);    
  	for(i=0;i<6;i++)     
  	  OLED_WrDat(F6x8[c][i]);  
  	x+=6;
  	j++;
  }
}

void OLED_Fill(unsigned char bmp_data)
{
	unsigned char y,x;
	for(y=0;y<8;y++)
	{
		OLED_WrCmd(0xb0+y);
		OLED_WrCmd(0x01);
		OLED_WrCmd(0x10);
		for(x=0;x<X_WIDTH;x++)
			OLED_WrDat(bmp_data);
	}
}

