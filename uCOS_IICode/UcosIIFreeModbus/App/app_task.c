#include "app_task.h"
#include "bsp.h"
#include "mb.h"

/**************************************************
函数名称 ： AppTask1
功    能 ： 应用任务1
参    数 ： p_arg --- 可选参数
返 回 值 ： 无
作    者 ： strongerHuang
*************************************************/
void LedTask(void *p_arg)
{
  for(;;)
  {
    LED=!LED;
    OSTimeDly(50);
  }
}

/************************************************
函数名称 ： AppTask2
功    能 ： 应用任务2
参    数 ： p_arg --- 可选参数
返 回 值 ： 无
作    者 ： strongerHuang
*************************************************/
void ComTask(void *p_arg)
{
  while(1)
  {
    (void)eMBPoll();
    OSTimeDly(10);
  }
}


